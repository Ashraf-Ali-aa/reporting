package com.qaprosoft.zafira.models.db;

public class Invitation extends AbstractEntity {

    private static final long serialVersionUID = -7507603908818483927L;

    private String email;
    private String token;
    private User createdBy;
    private Status status;
    private User.Source source;
    private Long groupId;
    private String invitationUrl;

    public enum Status {
        PENDING,
        ACCEPTED
    }

    public Invitation() {
    }

    public Invitation(String email, String token) {
        this.email = email;
        this.token = token;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public User getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(User createdBy) {
        this.createdBy = createdBy;
    }

    public Status getStatus() {
        return status;
    }

    public void setStatus(Status status) {
        this.status = status;
    }

    public User.Source getSource() {
        return source;
    }

    public void setSource(User.Source source) {
        this.source = source;
    }

    public Long getGroupId() {
        return groupId;
    }

    public void setGroupId(Long groupId) {
        this.groupId = groupId;
    }

    public String getInvitationUrl() {
        return invitationUrl;
    }

    public void setInvitationUrl(String invitationUrl) {
        this.invitationUrl = invitationUrl;
    }

    public boolean isValid() {
        return this.status != null && this.getStatus().equals(Status.PENDING);
    }

}
